  def dice_command(string, name)
    secret_flg = false

    return '1', secret_flg unless self.class.prefixesPattern =~ string

    secretMarker = Regexp.last_match(2)
    command = Regexp.last_match(3).upcase

    output_msg = getCommandResult(command, name)

    if secretMarker # 隠しロール
      secret_flg = true if output_msg != '1'
    end

    return output_msg, secret_flg
  end

  def check_nD6(total_n, dice_n, signOfInequality, diff, dice_cnt, _dice_max, _n1, _n_max) # ゲーム別成功度判定(nD6)
    if (dice_cnt == 3) && (signOfInequality == "<=")

      success = diff - total_n; # 成功度
      crt_string = " ＞ クリティカル(成功度：#{success})"
      fmb_string = " ＞ ファンブル(失敗度：#{success})"
      fail_string = " ＞ 自動失敗(失敗度：#{success})"

      # クリティカル
      if (dice_n <= 6) && (diff >= 16)
        return crt_string
      elsif (dice_n <= 5) && (diff >= 15)
        return crt_string
      elsif dice_n <= 4
        return crt_string
      end
      # ファンブル
      if diff - dice_n <= -10
        return fmb_string
      elsif (dice_n >= 17) && (diff <= 15)
        return fmb_string
      elsif dice_n >= 18
        return fmb_string
      elsif dice_n >= 17
        return fail_string
      end

      if total_n <= diff
        return " ＞ 成功(成功度：#{success})"
      else
        return " ＞ 失敗(失敗度：#{success})"
      end

    end

    return ''
  end

